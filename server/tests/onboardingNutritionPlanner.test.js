test('OnboardingNutritionMenuService normalizes imported menu periods and avoids duplicate inserts', async () => {
    const originalFindById = User.findById;
    const originalCountDocuments = NutritionMenu.countDocuments;
    const originalFind = NutritionMenu.find;
    const originalInsertMany = NutritionMenu.insertMany;
    const originalUpdateMany = NutritionMenu.updateMany;

    try {
        let saveCalls = 0;
        let insertedDocs = [];
        const fakeUser = {
            _id: 'user-3',
            profile: {
                onboarding_completed: true,
                nutrition_plan_choice: 'existing',
                nutrition_plan_status: 'pending',
                nutrition_plan_source: 'manual',
            },
            async save() {
                saveCalls += 1;
            },
        };

        User.findById = async () => fakeUser;
        NutritionMenu.countDocuments = async () => insertedDocs.filter((doc) => !doc.archived).length;
        NutritionMenu.find = (query = {}) => ({
            select() { return this; },
            lean: async () => {
                if (query.import_origin === 'onboarding_custom_menu') {
                    return insertedDocs
                        .filter((doc) => !doc.archived && doc.import_origin === 'onboarding_custom_menu')
                        .map((doc) => ({ import_signature: doc.import_signature }));
                }
                return [];
            },
        });
        NutritionMenu.insertMany = async (docs) => {
            insertedDocs.push(...docs.map((doc) => ({ ...doc, archived: false })));
            return docs;
        };
        NutritionMenu.updateMany = async () => {
            insertedDocs = insertedDocs.map((doc) => ({ ...doc, archived: true }));
            return { acknowledged: true, modifiedCount: insertedDocs.length };
        };

        const inputProfile = {
            ...fakeUser.profile,
            custom_nutrition_menu: [
                { meal_period: 'Breakfast', meal_name: 'Egg Bowl', total_calories: 420 },
                { meal_period: 'Lunch', meal_name: 'Chicken Rice', total_calories: 610 },
                { meal_period: 'Snack', meal_name: 'Protein Bar', total_calories: 240 },
                { meal_period: 'Dinner', meal_name: 'Steak Plate', total_calories: 680 },
            ],
        };

        const service = new OnboardingNutritionMenuService({
            menuRequest: { execute: async () => { throw new Error('should not call execute for existing path'); } },
            userModel: User,
            nutritionMenuModel: NutritionMenu,
        });

        const firstOutcome = await service.ensureMenuForUser({
            userId: 'user-3',
            requestId: 'req-3a',
            trigger: 'test',
            profile: inputProfile,
        });

        const secondOutcome = await service.ensureMenuForUser({
            userId: 'user-3',
            requestId: 'req-3b',
            trigger: 'test',
            profile: inputProfile,
        });

        assert.equal(firstOutcome.triggered, true);
        assert.equal(firstOutcome.reason, 'custom_menu_saved');
        assert.equal(secondOutcome.triggered, false);
        assert.equal(secondOutcome.reason, 'custom_menu_already_saved');
        assert.equal(insertedDocs.length, 4);
        assert.equal(insertedDocs.some((doc) => doc.meal_period === 'Pre-Workout Snack'), true);
        assert.equal(fakeUser.profile.nutrition_plan_status, 'ready');
        assert.equal(fakeUser.profile.meal_frequency, 4);
        assert.equal(saveCalls, 2);
    } finally {
        User.findById = originalFindById;
        NutritionMenu.countDocuments = originalCountDocuments;
        NutritionMenu.find = originalFind;
        NutritionMenu.insertMany = originalInsertMany;
        NutritionMenu.updateMany = originalUpdateMany;
    }
});
