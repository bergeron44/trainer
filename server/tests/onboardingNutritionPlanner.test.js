const test = require('node:test');
const assert = require('node:assert/strict');

const User = require('../models/User');
const NutritionMenu = require('../models/NutritionMenu');
const OnboardingNutritionMenuService = require('../services/onboardingNutritionMenuService');
const {
    AUTO_GENERATED_ONBOARDING_MENU_NOTE,
    isLegacyAutogeneratedNutritionMenuEntry,
} = require('../utils/nutritionMenuUtils');

test('isLegacyAutogeneratedNutritionMenuEntry matches old onboarding placeholder menus', () => {
    assert.equal(isLegacyAutogeneratedNutritionMenuEntry({
        source: 'ai',
        note: AUTO_GENERATED_ONBOARDING_MENU_NOTE,
        foods: [],
    }), true);

    assert.equal(isLegacyAutogeneratedNutritionMenuEntry({
        source: 'ai',
        note: AUTO_GENERATED_ONBOARDING_MENU_NOTE,
        foods: [{ name: 'Chicken Breast' }],
    }), false);
});

test('OnboardingNutritionMenuService creates a real AI menu when only legacy placeholders exist', async () => {
    const originalFindById = User.findById;
    const originalCountDocuments = NutritionMenu.countDocuments;
    const originalFind = NutritionMenu.find;

    try {
        let saveCalls = 0;
        let findCallIndex = 0;
        const fakeUser = {
            _id: 'user-1',
            profile: {
                onboarding_completed: true,
                nutrition_plan_choice: 'ai',
                nutrition_plan_status: 'pending',
                nutrition_plan_source: 'none',
                target_calories: 2400,
                protein_goal: 180,
                carbs_goal: 210,
                fat_goal: 70,
                diet_type: 'everything',
            },
            liked_foods: [],
            disliked_foods: [],
            async save() {
                saveCalls += 1;
            },
        };

        const mockMenuRequest = {
            execute: async () => ({
                createdCount: 4,
                provider: 'test',
                model: 'test-model',
                toolTrace: [],
                response: 'Created 4 menu entries.',
            }),
        };

        User.findById = async () => fakeUser;
        NutritionMenu.countDocuments = async () => 2;

        // First find: initial existingMenuEntries check → all legacy placeholders
        // Second find: savedEntries after generation → 4 real entries
        NutritionMenu.find = () => {
            const callIndex = findCallIndex++;
            return {
                select() { return this; },
                lean: async () => {
                    if (callIndex === 0) {
                        return [
                            { source: 'ai', note: AUTO_GENERATED_ONBOARDING_MENU_NOTE, foods: [] },
                            { source: 'ai', note: AUTO_GENERATED_ONBOARDING_MENU_NOTE, foods: [] },
                        ];
                    }
                    return [
                        { meal_period: 'Breakfast' },
                        { meal_period: 'Lunch' },
                        { meal_period: 'Pre-Workout Snack' },
                        { meal_period: 'Dinner' },
                    ];
                },
            };
        };

        const service = new OnboardingNutritionMenuService({
            menuRequest: mockMenuRequest,
            userModel: User,
            nutritionMenuModel: NutritionMenu,
        });

        const outcome = await service.ensureMenuForUser({
            userId: 'user-1',
            requestId: 'req-1',
            trigger: 'test',
            profile: fakeUser.profile,
        });

        assert.equal(outcome.triggered, true);
        assert.equal(outcome.status, 'ready');
        assert.equal(outcome.reason, 'ai_menu_generated');
        assert.equal(outcome.createdCount, 4);
        assert.equal(fakeUser.profile.nutrition_plan_status, 'ready');
        assert.equal(fakeUser.profile.nutrition_plan_source, 'agent');
        assert.ok(fakeUser.profile.nutrition_plan_generated_at instanceof Date);
        assert.equal(fakeUser.profile.meal_frequency, 4);
        assert.equal(saveCalls, 2); // once for 'generating', once for 'ready'
    } finally {
        User.findById = originalFindById;
        NutritionMenu.countDocuments = originalCountDocuments;
        NutritionMenu.find = originalFind;
    }
});

